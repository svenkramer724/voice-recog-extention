function setup_languages() { // Version 1.5.6
	for (var i = 0; i < languages.length; i++) {
		document.settings_form.select_language.options[i] = new Option(languages[i].Language, languages[i].Code);
	}  

	// Version 1.5.6 - locales with languages files
	var locales = {
		"en": "languages-en.js", 
		"es": "languages-es.js",
		"de": "languages-de.js",
		"ja": "languages-ja.js", // Version 1.5.9
		"fr": "languages-fr.js", // Version 1.6.0
		"it": "languages-it.js", // Version 1.6.1
		"hi": "languages-hi.js", // Version 1.7.2
	};
	var lang_code = chrome.i18n.getUILanguage() || "en";
	var lang_src = "languages-en.js"; // Default to english
	if (locales.hasOwnProperty(lang_code)) // Version 1.5.6 - if language_commands["en-US"]
		lang_code = lang_code; // then don't change
	else if (locales.hasOwnProperty(lang_code.split("-")[0])) // Version 1.5.6 - if language_commands["en"]
		lang_code = lang_code.split("-")[0]; //then change to "en"
	else if (lang_code = "yue-Hant-HK") // Version 1.5.6 - If Chinese Cantonese
		lang_code = "zh"; // then display Chinese commands

	if (locales.hasOwnProperty(lang_code))
		lang_src = locales[lang_code];

	// Version 1.5.6 - Load language-xx.js file
	var script = document.createElement('script');
	try {
		script.onload = function () {
			// Remove previous languages from select list first
			while(document.settings_form.select_language.length > 0)
				document.settings_form.select_language.remove(0);
			// Add translated languages to select list
			for (var i = 0; i < languages_translated.length; i++) {
				document.settings_form.select_language.options[i] = new Option(languages_translated[i].Language+" "+languages_translated[i].Code, languages_translated[i].Code); // Version 1.6.2 - Added +" "+languages_translated[i].Code
			}  
			setup_forms(); // Version 1.5.8 - Because the changed language was not in the form on refresh
			change_language(); // Version 1.5.8 - Because the changed language was not in the form on refresh
		};
		script.src = lang_src;
		document.head.appendChild(script); 
	} catch (error) {
		console.log(error);
	}
}


